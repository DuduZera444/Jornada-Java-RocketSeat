package com.rocketseat.controleDeFluxo;

public class LacosFor {

	public static void main(String[] args) {
		
		for (int i = 1; i <=5; i++) {
			System.out.println("Contagem " + i);
		}
		
		/*O laço FOR é um laço de repetição, ele atende uma condição e ate que tal condição se torne falsa 
		 * ele continua executando oque foi definido no bloco como no excemplo acima
		 * int i começa com o numero 1, enquanto i for menor ou igual a 5 ele continua e quando chegar no fim
		 * o bloco de codigo para de ser executado e ele sair do laço de repetição.
		 * */
	}

}
