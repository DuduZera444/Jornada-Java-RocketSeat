package com.rocketseat.operadores;

public class OperadoresAritmeticoseAtribuição {

	public static void main(String[] args) {
		
		// o sinal de igual ('=') é o sinal de atribuição
		String nome = "Gleyson";
		
		// o sinal de soma ('+') serve pra somar mas também para concatenar 
		int soma = 1 + 1;
		
		// o sinal de negativo ('-') faz a subtração de valores
		int sub = 2 - 1;
		
		// a estrela ('*') faz a multiplicação de valores
		int multi = 2 * 2;
		
		// a barra ('/') é responsavel pela divisão
		int div = 8 / 2;
		
		// o modulo ('%') entrega o resto de uma divisão
		int mod = 5 % 2;

	}

}
